package org.activiti.services.query.app.predicates;

import com.querydsl.core.types.dsl.BooleanExpression;
import org.activiti.services.query.app.model.ProcessInstance;
import org.activiti.services.query.app.util.SearchCriteria;
import com.querydsl.core.types.dsl.NumberPath;
import com.querydsl.core.types.dsl.PathBuilder;
import com.querydsl.core.types.dsl.StringPath;

import static org.apache.commons.lang3.StringUtils.isNumeric;

public class ProcessInstancePredicate {

    private SearchCriteria criteria;

    public ProcessInstancePredicate(final SearchCriteria criteria) {
        this.criteria = criteria;
    }

    public BooleanExpression getPredicate() {
        final PathBuilder<ProcessInstance> entityPath = new PathBuilder<>(ProcessInstance.class, "processInstance");

        if (isNumeric(criteria.getValue().toString())) {
            final NumberPath<Integer> path = entityPath.getNumber(criteria.getKey(), Integer.class);
            final int value = Integer.parseInt(criteria.getValue().toString());
            switch (criteria.getOperation()) {
                case ":":
                    return path.eq(value);
                case ">":
                    return path.goe(value);
                case "<":
                    return path.loe(value);
            }
        } else {
            final StringPath path = entityPath.getString(criteria.getKey());
            if (criteria.getOperation().equalsIgnoreCase(":")) {
                return path.containsIgnoreCase(criteria.getValue().toString());
            }
        }
        return null;
    }
}